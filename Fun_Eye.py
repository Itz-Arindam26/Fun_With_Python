import turtle as tur
a = tur.Turtle()
a.speed(1)
a.hideturtle()
a.penup()
a.forward(85)
a.lt(90)
a.forward(80)
a.pendown()
def eyes():
    a.circle(85)
    a.fillcolor("black")
    a.begin_fill()
    a.circle(45)
    a.end_fill()

eyes()
a.penup()
a.rt(90)
a.forward(30)
a.lt(90)
a.pendown()
a.rt(180)
eyes()